// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, testFunc, updateFunc, bodyFunc) {
  for (let i = value; testFunc(i); i = updateFunc(i)){
    bodyFunc(i)
  }
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(array, test) {
for (var i = 0; i < array.length; i++){
  if (test(array[i]) === false){
    return false
  } 
} return true 
}


// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1, rangeArray = []) {

  //base case
  
  if (step < 0 && start < end) return rangeArray

  if (start === end)  {
    if (rangeArray.length === 0) return rangeArray
    rangeArray.push(end)
    return rangeArray
    
  }


  if (start < end) {
    rangeArray.push(start)
    return range(start + step, end, step, rangeArray)
  }

  //recursion
  if (start > end) {
    rangeArray.push(start )
    return range(start - step, end, step, rangeArray)
  }
  
}




